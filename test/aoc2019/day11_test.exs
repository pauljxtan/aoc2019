defmodule Aoc2019.Day11Test do
  use ExUnit.Case

  test "part 1" do
    assert Aoc2019.Day11.solve_part1() == 1732
  end

  test "part 2" do
    test_panels = %{
      {-1, -1} => :black,
      {-1, 0} => :white,
      {-1, 2} => :black,
      {0, -1} => :white,
      {0, 0} => :white,
      {0, 1} => :black,
      {1, -1} => :black,
      {1, 0} => :white,
      {1, 1} => :white,
      {1, 2} => :white,
      {2, -1} => :black,
      {2, 1} => :black
    }

    assert test_panels |> Aoc2019.Day11.format_str() == [".#..", "###.", "..#.", "..#."]

    assert Aoc2019.Day11.solve_part2() == [
             "..##..###...##..#....####.#..#.#..#...##...",
             ".#..#.#..#.#..#.#....#....#..#.#..#....#...",
             ".#..#.###..#....#....###..#..#.####....#...",
             ".####.#..#.#....#....#....#..#.#..#....#...",
             ".#..#.#..#.#..#.#....#....#..#.#..#.#..#...",
             ".#..#.###...##..####.#.....##..#..#..##...."
           ]
  end
end
